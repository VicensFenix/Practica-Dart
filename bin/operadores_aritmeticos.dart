void main() {
  //Declarar variables
  double num1 = 12;
  double num2 = 22;

  //Suma
  double suma = num1 + num2;
  print("El resultdo es: $num1 + $num2 = $suma");

  //Resta
  double resta = num1 - num2;
  print("El resultado es: $num1 - $num2 = $resta");

  //Multiplicación
  double multiplicacion = num1 * num2;
  print("El resultado es: $num1 * $num2 = $multiplicacion");

  //División
  double division = num1 / num2;
  print("El resultado es: $num1 / $num2 = $division");

  //Modulo
  double modulo = num1 % num2;
  print("El resultado es: $num1 % $num2 = $modulo");
}
