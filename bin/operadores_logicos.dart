void main() {
  int marcos = 18;
  int renata = 17;

  if (marcos >= 18 && renata >= 18) {
    print("Ambos son mayores de edad");
  } else {
    print("Uno no es mayor de edad");
  }

  if (marcos >= 18 || renata >= 18) {
    print("Uno es mayor de edad");
  } else {
    print("Ambos son menores de edad");
  }
}
