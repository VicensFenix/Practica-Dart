// Para convertir dólares a yenes, necesitas conocer la tasa de cambio actual entre el dólar estadounidense (USD) y el yen japonés (JPY). Esta tasa varía con el tiempo, pero puedes usar un valor aproximado o buscar la tasa actual en línea.

void main() {
  double usd = 100.0;
  double tasaDeCambio = 110.0;

  double jpy = usd * tasaDeCambio;
  print("$usd USD es igual a $jpy JPY");
}
